      id: 'food',
      emoji: '🍲',
      title: 'Твоё блюдо сегодня',
      text: `Стихия ${element} и ${season} подсказывают: сегодня твоё блюдо — ${user.food || 'что-то тёплое и домашнее'}. Побалуй себя.`,
