// ── Определить знак зодиака по дате ──────────────────
export function getZodiac(birthDate) {
  const d = new Date(birthDate)
  const m = d.getMonth() + 1
  const day = d.getDate()
  for (const z of ZODIAC_SIGNS) {
    const [start, end] = z.dates
    if ((m === start[0] && day >= start[1]) || (m === end[0] && day <= end[1])) {
      return z
    }
  }
  return ZODIAC_SIGNS[0] // fallback Козерог
}

// ── Нумерологическое число жизненного пути ───────────
export function getLifePathNumber(birthDate) {
  const str = birthDate.replace(/-/g, '')
  let sum = str.split('').reduce((a, c) => a + parseInt(c), 0)
  while (sum > 9 && sum !== 11 && sum !== 22 && sum !== 33) {
    sum = String(sum).split('').reduce((a, c) => a + parseInt(c), 0)
  }
  return sum > 9 ? sum % 9 || 9 : sum // мастер-числа упрощаем для MVP
}

