// ── Сезон года ────────────────────────────────────────
export function getSeason(date = new Date()) {
  const m = date.getMonth() + 1
  if (m >= 3 && m <= 5)  return 'весна'
  if (m >= 6 && m <= 8)  return 'лето'
  if (m >= 9 && m <= 11) return 'осень'
  return 'зима'
}

